# Calculadora Python simples

number_1 = float(input('Ensira o primeiro número desejado:'))
number_2 = float(input('Ensira o segundo número desejado:'))

soma = number_1 + number_2
subtração = number_1 - number_2
multiplicação = number_1 * number_2
divisão = number_1 / number_2

print(f"Soma: {soma}")
print(f"Subtração: {subtração}")
print(f"Multiplicação: {multiplicação}")
print(f"Divisão: {divisão}")